        public string Guardar_in(E_Uso_Insumos oUI, DataTable DT_DET)
        {
            string Rpta = "";
            SqlConnection SqlCon = new SqlConnection();
            try
            {
                SqlCon = Conexion.getInstancia().CrearConexion();
                SqlCommand Comando = new SqlCommand("Usp_guardar_in", SqlCon);
                Comando.CommandType = CommandType.StoredProcedure;
                Comando.Parameters.Add("@Ncodigo_al", SqlDbType.Int).Value = oUI.Codigo_al;
                Comando.Parameters.Add("@Ffecha_in", SqlDbType.Date).Value = oUI.Fecha_in;
                Comando.Parameters.Add("@Creferencia_in", SqlDbType.VarChar).Value = oUI.Referencia_in;
                Comando.Parameters.Add("@Cobservacion_in", SqlDbType.Text).Value = oUI.Observacion_in;
                Comando.Parameters.Add("@Ncodigo_us", SqlDbType.Int).Value = oUI.Codigo_us; 
                Comando.Parameters.Add("@DT_DET", SqlDbType.Structured).Value = DT_DET;
                SqlParameter ParCodigo = new SqlParameter();
                ParCodigo.ParameterName = "@Ncodigo_in";
                ParCodigo.SqlDbType = SqlDbType.Int;
                ParCodigo.Direction = ParameterDirection.Output;
                Comando.Parameters.Add(ParCodigo);
                SqlCon.Open();
                Comando.ExecuteNonQuery();
                Rpta = Convert.ToString(ParCodigo.Value);
            }
            catch (Exception ex)
            {
                Rpta = ex.Message;
            }
            finally
            {
                if (SqlCon.State == ConnectionState.Open) SqlCon.Close();
            }
            return Rpta;
        }

        public string Eliminar_in(int Ncodigo)
